import pandas as pd
import joblib

# Load already trained model assets
model = joblib.load("rf_churn_model.pkl")
scaler = joblib.load("rf_churn_scaler.pkl")
columns = joblib.load("rf_churn_columns.pkl")

# === Sample input: fill these based on your feature columns and data ===
sample_customer = {
    'Age': 40,
    'Gender': 'Female',
    'Tenure': 53,
    'MonthlyCharges': 49.81,
    'ContractType': 'Two-Year',
    'InternetService': 'Fiber Optic'
}

# Turn into DataFrame
sample_df = pd.DataFrame([sample_customer])

# One-hot encode to match training preprocessing
sample_df = pd.get_dummies(sample_df)

# Add/match all missing columns (to guarantee proper format/order)
for col in columns:
    if col not in sample_df.columns:
        sample_df[col] = 0
sample_df = sample_df[columns]

# Scale features
sample_scaled = scaler.transform(sample_df)

# Predict
pred = model.predict(sample_scaled)[0]
prob = model.predict_proba(sample_scaled)[0][1] # probability for 'Churn' class

print(f"Prediction: {'Churn' if pred == 1 else 'No Churn'}")
print(f"Probability of Churn: {prob:.2f}")

