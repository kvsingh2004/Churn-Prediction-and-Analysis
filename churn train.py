# --- churn_pipeline.py ---
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import classification_report, confusion_matrix
from imblearn.over_sampling import SMOTE
import joblib

# Load data (update path as needed)
df = pd.read_csv('/content/drive/MyDrive/Projects/CHURN/customer_churn_data.csv')

# Basic preprocessing (drop ID if present)
X = df.drop(['Churn', 'CustomerID'], axis=1, errors='ignore')
# Ensure binary encoding for target
y = df['Churn'].map({'No':0, 'Yes':1})

# One-hot encode categorical features
X = pd.get_dummies(X)

# Split train/test
X_train, X_test, y_train, y_test = train_test_split(X, y, stratify=y, test_size=0.2, random_state=42)

# --- Handle imbalance with SMOTE ---
sm = SMOTE(random_state=42)
X_res, y_res = sm.fit_resample(X_train, y_train)

# --- Scale features ---
scaler = StandardScaler()
X_res_scaled = scaler.fit_transform(X_res)
X_test_scaled = scaler.transform(X_test)

# --- Train model with class_weight also ---
model = RandomForestClassifier(n_estimators=100, class_weight='balanced', random_state=42)
model.fit(X_res_scaled, y_res)

# --- Evaluate properly ---
y_pred = model.predict(X_test_scaled)
print("Classification Report:\n", classification_report(y_test, y_pred))
print("Confusion Matrix:\n", confusion_matrix(y_test, y_pred, labels=[0,1]))

# --- Save assets for app ---
joblib.dump(model, "rf_churn_model.pkl")
joblib.dump(scaler, "rf_churn_scaler.pkl")
joblib.dump(list(X.columns), "rf_churn_columns.pkl")

